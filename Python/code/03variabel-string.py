# Variabel String

# String dalam Python diidentifikasi
# sebagai serangkaian karakter yang berdekatan
# yang diwakili dalam tanda kutip.

nilaiString = "saya pergi ke pasar"


# s a y a   p e r g i
# 0 1 2 3 4 5 5 6 7 8 9

# k  e     p  a  s  a  r
# 10 11 12 13 14 15 16 17


# print secara keseluruhan
print(nilaiString)
# print karakter pertama pada pesan
print(nilaiString[0])
# print karakter dari karakter ke 2 sampai 5
print(nilaiString[2:6])
# print dimulai dari karakter ke 2
print(nilaiString[2:])
# print kata 2 kali
print(nilaiString * 2)