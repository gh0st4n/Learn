# Data Type
"""
Tipe Data adalah klasifikasi data. Tipe data menetukan jenis sebuah variabel
"""
# Berupa Text
## str
"""
`str` merupakan tipe data yang berupa string, berfungsi untuk menghasilkan tipe data string seperti huru atau sebagai nya dalam bentuk string.
"""

nama:str = "Muhammad Luthfi Fakhmar"
print(f"Var str : {nama}")
print(f"{type(nama)}\n")

# Berupa Angka
## int
"""
`int` merupakan salah satu tipe data numerik yang sering digunakan berupa bilangan bulat.
"""

angka:int = 17
print(f"Var int: {angka}")
print(f"type(angka)\n")

